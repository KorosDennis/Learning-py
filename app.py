#Variables 
#Variables are one of the fundamental building blocks of programs written in Python. Variables hold data in memory. 
# They have names, and they can be referenced by those names.
#  Variables also have types, which specify what type of data they can store (such as string and integer),
#  and they can be used in expressions that use operators (such as + and -) to manipulate their values.

# In Python, a variable is declared and assigned a value using the assignment operator =. 
# The variable is on the left-hand side of the operator, 
# and the value being assigned—which can be an expression such as 2 + 2 
# and can even include other variables—is on the right-hand side.
#  For example:
x = 1         # assign variable x the value 1
y = x + 5     # assign variable y the value of x plus 5
z = y         # assign variable z the value of y

# These examples assign numbers to variables, but numbers are just one of several data types Python supports.
#  Notice there's no type declared for the variables.
#  Python is a dynamically typed language, meaning the variable type is determined by the data assigned to it.
#  In the previous examples, the x, y, and z variables are integer types, capable of storing positive and negative whole numbers.
# Variable names are case sensitive and can use any letter, number, and the underscore (_) character. However, they can't start with a number.